function parseDate(str){
	var parts = str.split(" "),
		dateParts = parts[0].split("-"),
		timeParts = parts[1].split(":");
	return new Date(
		dateParts[2],
		dateParts[1] - 1,
		dateParts[0],
		timeParts[0],
		timeParts[1]
	);
}

